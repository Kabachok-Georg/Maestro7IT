def count_squares():
    # Количество клеток, которые могут быть закрашены слонами
    # Слоны в углах закрашивают клетки по диагоналям
    # На шахматной доске 8x8
    # Каждая диагональ в пределах доски имеет уникальные клетки

    # Клетки, которые могут быть закрашены (по диагоналям)
    covered_squares = set()

    # Слоны в углах
    positions = [(1, 1), (1, 8), (8, 1), (8, 8)]

    for x, y in positions:
        # Добавляем клетки по диагонали (слева-верх, справа-верх, слева-низ, справа-низ)
        for d in range(1, 7):
            if 1 <= x + d <= 8 and 1 <= y + d <= 8:
                covered_squares.add((x + d, y + d))  # Диагональ вниз-вправо
            if 1 <= x + d <= 8 and 1 <= y - d <= 8:
                covered_squares.add((x + d, y - d))  # Диагональ вниз-влево
            if 1 <= x - d <= 8 and 1 <= y + d <= 8:
                covered_squares.add((x - d, y + d))  # Диагональ вверх-вправо
            if 1 <= x - d <= 8 and 1 <= y - d <= 8:
                covered_squares.add((x - d, y - d))  # Диагональ вверх-влево

    # Вывод количества закрашенных клеток
    print(len(covered_squares))

count_squares()

